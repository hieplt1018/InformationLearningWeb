package model;

public class Diem {

    private int maHK;
    private String maSV;
    private String maMH;
    private String tenMH;
    private String soTC;
    private String diemCC;
    private String diemKT;
    private String diemBT;
    private String diemTH;
    private String diemThi;
    private String diemTK;
    private String diemChu;

    public Diem(int maHK, String maSV, String maMH, String tenMH, String soTC, String diemCC, String diemKT, String diemBT, String diemTH, String diemThi, String diemTK, String diemChu) {
        this.maHK = maHK;
        this.maSV = maSV;
        this.maMH = maMH;
        this.tenMH = tenMH;
        this.soTC = soTC;
        this.diemCC = diemCC;
        this.diemKT = diemKT;
        this.diemBT = diemBT;
        this.diemTH = diemTH;
        this.diemThi = diemThi;
        this.diemTK = diemTK;
        this.diemChu = diemChu;
    }

    public int getMaHK() {
        return maHK;
    }

    public void setMaHK(int maHK) {
        this.maHK = maHK;
    }

    public String getMaSV() {
        return maSV;
    }

    public void setMaSV(String maSV) {
        this.maSV = maSV;
    }

    public String getMaMH() {
        return maMH;
    }

    public void setMaMH(String maMH) {
        this.maMH = maMH;
    }

    public String getTenMH() {
        return tenMH;
    }

    public void setTenMH(String tenMH) {
        this.tenMH = tenMH;
    }

    public String getSoTC() {
        return soTC;
    }

    public void setSoTC(String soTC) {
        this.soTC = soTC;
    }

    public String getDiemCC() {
        return diemCC;
    }

    public void setDiemCC(String diemCC) {
        this.diemCC = diemCC;
    }

    public String getDiemKT() {
        return diemKT;
    }

    public void setDiemKT(String diemKT) {
        this.diemKT = diemKT;
    }

    public String getDiemBT() {
        return diemBT;
    }

    public void setDiemBT(String diemBT) {
        this.diemBT = diemBT;
    }

    public String getDiemTH() {
        return diemTH;
    }

    public void setDiemTH(String diemTH) {
        this.diemTH = diemTH;
    }

    public String getDiemThi() {
        return diemThi;
    }

    public void setDiemThi(String diemThi) {
        this.diemThi = diemThi;
    }

    public String getDiemTK() {
        return diemTK;
    }

    public void setDiemTK(String diemTK) {
        this.diemTK = diemTK;
    }

    public String getDiemChu() {
        return diemChu;
    }

    public void setDiemChu(String diemChu) {
        this.diemChu = diemChu;
    }

 
}
